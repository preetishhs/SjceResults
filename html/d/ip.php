Department of Industrial and Production Engineering<br><br>

The Department of Industrial and Production Engineering was started in the year 1980 and has gained the Grant in Aid in the year 1983. The Department is presently offering Bachelor of Engineering Course in Industrial and Production Engineering with an annual intake of 60 students. The syllabus is framed to suit the needs of Industries. 
<br><br>
The Department has the following Laboratories. They are<br>
1.	 Metrology Laboratory<br>
2.	 CAD/CAM & CIM Laboratory<br>
3.	 Industrial Engineering Laboratory<br><br>

The Metrology Laboratory is equipped with Profile Projector, Talyrond, Autocollimator, Tool Makers, Microscope, Dial Micrometer, Optical Flats, Levelling Press, Gear Tooth Micrometer, Spline Micrometer, Deep Hole Dial Depth Guage, Dial Micrometer, V-Anvil Micrometer, Magnetic Check Profile Projector, Vernier Calipers, Dial Calipers, Digital Calipers for carrying out various experiments relating to calibration and testing of the instruments.
<br>
The CAD/CAM labs are equipped with 80 computers and the syllabus covers advanced Modelling and Simulation features throughout the semesters. <br>
The Computer related laboratories are CAD/CAM & CIM lab, CAE Lab, Industrial Engineering Lab, Software Applications Lab and Drafting Lab where the students learn advanced software like Solid Edge, Synchronous technology, UGS NX6, ANSYS 11, CNC EXSL Simulation software, Matlab, WinQSB, Proplanner, Workstudy Professional, Oracle, Minitab and SPSS. In all the semesters, the students will learn computer related software. In addition, the students are encouraged to carry out PACE projects using UGS NX software. <br>

The Industrial Engineering Lab is equipped with a Modern Tread Mill and Ergometer for ergonomic studies and the softwares like Proplanner and Timer Pro is used for carrying out studies on time study simulation & analysis, Line balancing, Value Stream mapping, Kaizen, Poka yoke, EBOM, MBOM analysis, Plant Layout etc. The table experiments are being carried out using the standard facilities available in the laboratory.
<br>
The department is a recognized research center and is guiding students to carry out Ph.D projects. So far the Department has produced 3 students with Doctoral Degree and 5 students are carrying out Doctoral programme. The guides are Dr. K.V.Sreenivasa Prasad, Dr. T.R.Srinivas and Dr. V.Ramesh.
The department is actively doing research and has produced several papers in International Journals and conferences.
