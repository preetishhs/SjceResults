The information Science and Engineering programme was started in the year 2000 with an approval from AICTE. The department offers bachelors degree in Information Science & Engineering. The department has an intake of 60 students as per AICTE norms. The department has a highly motivated, competent and distinguished faculty. Research is an integral part of departmental activities where all the faculty members are involved. The department has excellent research facilities, which cater to the needs of the research community. The department has a well-focused research area, which provides opportunity for the students to implement and test their skills.
<br><br>
The Bachelor of Engineering Programme:<br>
The department offers bachelors degree in Information Science & Engineering which has a comprehensive curriculum approved by VTU. The entire course is spread over 8 semesters with the right mix of theory subjects, practical sessions, project works and seminar presentations.
<br>
The first two semesters cover subjects, which are general in nature. Subjects in basic sciences like Physics, Chemistry and Mathematics as well as courses in basic engineering like Electrical Engineering, Mechanical Engineering, Electronics & Instrumentation, Workshop Practice and Engineering Drawing are covered.
<br>
The next five semesters cover various areas of Information Science in a systemic way. Adequate scope has been provided to the students to choose electives in different streams of interest starting from the 6th semester onwards. Topics covered include:
<br><br>
	1.	Electronics and Hardware related courses<br>
	2.	Programming related courses<br>
	3.	Theoretical and software related courses<br>
	4.	Communication and Network related courses<br>
	5.	Advanced courses<br>
<br>
Subjects taught are well supported by laboratory sessions. List of experiments conducted are continually updated to keep it dynamic and abreast the latest developments in the field. The final semester consists of a project work usually undertaken in leading industries and premier research organizations. The students are also required to present a seminar choosing a current topic from leading journals so as to hone up their presentation / communication skills. In summary the department strives hard to provide every opportunity that meets the requirement of Information Technology Industries.
